import math
pi = 3.14
numOfArray = [2, 4, 6]

print(math.ceil(pi)) #round up if it has decimal, except for 0 (whole number)
print(math.floor(pi)) #round down, stays to the whole number
print (math.pow(pi, 2)) #power of 2
print (round(pi)) #roundoff
print (math.sqrt(420))  #square root of 420
print(max(numOfArray)) #6
print(min(numOfArray)) #2